    function howManyDalmatians(numer) {
        return numer <= 10 ? "Hardly any"
            : numer <= 50 ? "More than a handful!"
                : numer === 101 ? "101 DALMATIANS!!!"
                    : "Woah that's a lot of dogs!"
    }